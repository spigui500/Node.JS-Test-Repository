//code for changing to all lowercase//
const uppernames = ['Matthew', 'Dylan', 'Desmond', 'Ryan', 'Ronan'];
const lowerCaseNames = uppernames.map(name => name.toLowerCase());
console.log(lowerCaseNames);

//code for changing to uppercase//
const lowernames = ['matthew', 'dylan', 'desmond', 'ryan', 'ronan'];
const upperCaseNames = lowernames.map(name => name.toUpperCase());
console.log(upperCaseNames);